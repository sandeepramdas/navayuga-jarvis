export function getFleetByProject(projectId: string): Machine[] {
  return fleet.filter(m => m.projectId === projectId)
}

export function getFleetSummary() {
  const active = fleet.filter(m => m.status === 'active').length
  const idle = fleet.filter(m => m.status === 'idle').length
  const maintenance = fleet.filter(m => m.status === 'maintenance').length
  const avgUtilization = Math.round(fleet.reduce((s, m) => s + m.utilization, 0) / fleet.length)
  const totalIdleCost = fleet.filter(m => m.status === 'idle').reduce((s, m) => s + m.dailyIdleCost, 0)
  const criticalMaintenance = fleet.filter(m => m.predictiveMaintenance.riskLevel === 'critical').length
  return { active, idle, maintenance, avgUtilization, totalIdleCost, criticalMaintenance, total: fleet.length }
}
